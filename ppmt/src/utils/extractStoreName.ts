/**
 * 从storeName字符串中提取店铺名称
 * 根据中国城市名称来定位并提取店铺名称
 * @param storeName 原始storeName字符串
 * @returns 提取出的店铺名称，如果提取失败则返回空字符串（表格会显示"-"）
 */
export function extractStoreName(storeName: string): string {
  if (!storeName || typeof storeName !== 'string') {
    return storeName || '';
  }

  // 按顿号、逗号等分隔符分割字符串
  const parts = storeName.split(/[、，,]/).map(part => part.trim()).filter(part => part);

  // 查找包含城市名称的部分
  // 按城市名称长度从长到短排序，避免短城市名匹配到长城市名的一部分（如"北京"匹配到"北京市"）
  const sortedCities = [...CHINESE_CITIES].sort((a, b) => b.length - a.length);
  
  for (let i = 0; i < parts.length; i++) {
    const part = parts[i];
    
    // 检查这个部分是否包含城市名称
    for (const city of sortedCities) {
      if (part.includes(city)) {
        // 找到包含城市的片段，提取城市及其后面的内容作为店铺名称
        const cityIndex = part.indexOf(city);
        if (cityIndex !== -1) {
          // 提取从城市开始到该片段结束的内容
          const storeNamePart = part.substring(cityIndex);
          
          // 如果城市后面还有内容，返回城市+后面的内容（这就是店铺名称）
          if (storeNamePart.length > city.length) {
            return storeNamePart;
          } 
          // 如果城市后面没有内容，但还有下一个片段，尝试合并
          else if (i + 1 < parts.length && parts[i + 1]) {
            const nextPart = parts[i + 1];
            // 如果下一个片段不是明显不是店铺名称的内容，则合并
            const excludePatterns = [
              /^暂无/, 
              /^可用\d+积分/, 
              /^提示/, 
              /不支持/, 
              /退货/,
            ];
            if (!excludePatterns.some(pattern => pattern.test(nextPart))) {
              return city + nextPart;
            }
          }
          // 如果城市后面没有内容且没有合适的下一个片段，只返回城市
          else {
            return city;
          }
        }
      }
    }
  }

  // 如果没有找到城市，直接返回空字符串
  // 店铺名称应该总是包含城市名称，如果没有城市名称，说明无法确定店铺名称
  return '';
}

export default extractStoreName;

